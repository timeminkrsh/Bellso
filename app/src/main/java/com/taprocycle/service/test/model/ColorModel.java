package com.taprocycle.service.test.model;

public class ColorModel {
    private String colour_name;
    private String colour_image;
    private String stock;
    private String id;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getColour_name() {
        return colour_name;
    }

    public void setColour_name(String colour_name) {
        this.colour_name = colour_name;
    }

    public String getColour_image() {
        return colour_image;
    }

    public void setColour_image(String colour_image) {
        this.colour_image = colour_image;
    }

    public String getStock() {
        return stock;
    }

    public void setStock(String stock) {
        this.stock = stock;
    }
}
